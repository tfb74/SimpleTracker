@Observable
final class UserSettings {
    static let shared = UserSettings()

    // Appearance
    var unitPreference: UnitPreference {
        didSet { save("unitPreference", unitPreference.rawValue) }
    }
    var colorScheme: AppColorScheme {
        didSet { save("colorScheme", colorScheme.rawValue) }
    }
    var appLanguage: AppLanguage {
        didSet { save("appLanguage", appLanguage.rawValue) }
    }
    var gameCenterSyncEnabled: Bool {
        didSet { save("gameCenterSyncEnabled", gameCenterSyncEnabled) }
    }
    var profileName: String {
        didSet { save("profileName", profileName) }
    }
    var avatarPreset: ProfileAvatarPreset {
        didSet { save("avatarPreset", avatarPreset.rawValue) }
    }
    var avatarImageData: Data? {
        didSet { saveData("avatarImageData", avatarImageData) }
    }

    // Profile for Score calculation
    var ageYears: Int {
        didSet { save("ageYears", ageYears) }
    }
    var weightKg: Double {
        didSet {
            save("weightKg", weightKg)
            // Nach Health zurückschreiben, wenn sich der Wert real geändert hat.
            // Wir schreiben nur, wenn noch kein Health-Gewicht vorhanden ist
            // oder der neue Wert signifikant (>0.2 kg) abweicht — vermeidet
            // doppelte Samples beim Laden.
            if weightKg > 0, weightKg != oldValue {
                let new = weightKg
                #if os(iOS)
                Task { await HealthKitService.shared.writeBodyMass(kg: new) }
                #endif
            }
        }
    }
    var heightCm: Double {
        didSet {
            save("heightCm", heightCm)
            if heightCm > 0, heightCm != oldValue {
                let new = heightCm
                #if os(iOS)
                Task { await HealthKitService.shared.writeHeight(cm: new) }
                #endif
            }
        }
    }

    // Computed
    var bmi: Double {
        guard heightCm > 0 else { return 0 }
        let h = heightCm / 100
        return weightKg / (h * h)
    }

    var bmiCategory: BMICategory {
        switch bmi {
        case ..<18.5:  return .underweight
        case 18.5..<25: return .normal
        case 25..<30:  return .overweight
        default:       return .obese
        }
    }

    var profileComplete: Bool { ageYears > 0 && weightKg > 0 && heightCm > 0 }

    /// Setzt einen sinnvollen Standard-Profilnamen wenn noch keiner existiert.
    /// Reihenfolge:
    /// 1. Geräte-Namen (z.B. „Felix's iPhone" → „Felix") — wenn der nichts
    ///    sinnvolles ergibt (iOS 16+ default ist nur „iPhone"), fallback…
    /// 2. Game-Center-Name (wenn GC eingeloggt) — kann später per
    ///    `applyGameCenterNameIfBetter` nachgereicht werden
    /// 3. Lustiger Zufallsname („Adler-712") als letzte Notlösung
    func ensureProfileDefaults(deviceName: String, gameCenterName: String? = nil) {
        guard profileName.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty else { return }
        if let suggested = Self.suggestedProfileName(from: deviceName), !suggested.isEmpty {
            profileName = suggested
            return
        }
        if let gc = gameCenterName?.trimmingCharacters(in: .whitespacesAndNewlines), !gc.isEmpty {
            profileName = gc
            return
        }
        profileName = Self.randomFunName()
    }

    /// Aufruf nach erfolgreichem Game-Center-Login. Wenn der Profilname noch
    /// auf einem Default basiert (leer, „iPhone", Random-Fun-Name), wird er
    /// durch den GC-Namen ersetzt — sonst nicht (User-Anpassung respektieren).
    func applyGameCenterNameIfBetter(_ gcName: String?) {
        let trimmed = gcName?.trimmingCharacters(in: .whitespacesAndNewlines) ?? ""
        guard !trimmed.isEmpty else { return }
        let current = profileName.trimmingCharacters(in: .whitespacesAndNewlines)
        if current.isEmpty || Self.isWeakDefaultName(current) {
            profileName = trimmed
        }
    }

    /// Erkennt offensichtliche Default-Namen die wir gerne ersetzen würden.
    static func isWeakDefaultName(_ name: String) -> Bool {
        let lowered = name.lowercased()
        let weak = ["iphone", "ipad", "apple watch", "simpletracking", "watch"]
        if weak.contains(lowered) { return true }
        // Random-Fun-Pattern: zwei-Wort-Tier + 3-stellige Zahl, z.B. „Adler-712"
        let regex = try? NSRegularExpression(pattern: #"^[A-ZÄÖÜ][a-zäöüß]+-\d{3}$"#)
        if let regex,
           regex.firstMatch(in: name, range: NSRange(name.startIndex..., in: name)) != nil {
            return true
        }
        return false
    }

    func effectiveProfileName(fallbackName: String) -> String {
        let trimmed = profileName.trimmingCharacters(in: .whitespacesAndNewlines)
        if !trimmed.isEmpty, !Self.isWeakDefaultName(trimmed) { return trimmed }

        let fallback = fallbackName.trimmingCharacters(in: .whitespacesAndNewlines)
        if !fallback.isEmpty, !Self.isWeakDefaultName(fallback) { return fallback }

        // Letzter Strohhalm: was wir gespeichert haben, selbst wenn schwach
        if !trimmed.isEmpty { return trimmed }
        if !fallback.isEmpty { return fallback }
        return lt("SimpleTracking")
    }

    private init() {
        let ud = UserDefaults.standard
        unitPreference = UnitPreference(rawValue: ud.string(forKey: "unitPreference") ?? "") ?? .metric
        colorScheme    = AppColorScheme(rawValue: ud.string(forKey: "colorScheme") ?? "") ?? .system
        appLanguage    = AppLanguage(rawValue: ud.string(forKey: "appLanguage") ?? "") ?? .system
        gameCenterSyncEnabled = ud.bool(forKey: "gameCenterSyncEnabled")
        profileName    = ud.string(forKey: "profileName") ?? ""
        avatarPreset   = ProfileAvatarPreset(rawValue: ud.string(forKey: "avatarPreset") ?? "") ?? .person
        avatarImageData = ud.data(forKey: "avatarImageData")
        ageYears       = ud.integer(forKey: "ageYears")
        weightKg       = ud.double(forKey: "weightKg")
        heightCm       = ud.double(forKey: "heightCm")
    }

    private func save(_ key: String, _ value: some Any) {
        UserDefaults.standard.set(value, forKey: key)
    }

    private func saveData(_ key: String, _ value: Data?) {
        UserDefaults.standard.set(value, forKey: key)
    }

    /// Versucht aus dem Gerätenamen einen Vornamen abzuleiten. Gibt nil zurück
    /// wenn das Resultat ein „weak default" wäre (nur „iPhone", „iPad").
    private static func suggestedProfileName(from deviceName: String) -> String? {
        let trimmed = deviceName.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmed.isEmpty else { return nil }

        for suffix in ["'s iPhone", "s iPhone", " iPhone", "'s iPad", "s iPad", " iPad", "'s Apple Watch", " Apple Watch"] {
            if trimmed.hasSuffix(suffix) {
                let cleaned = trimmed.replacingOccurrences(of: suffix, with: "")
                    .trimmingCharacters(in: .whitespacesAndNewlines)
                if !cleaned.isEmpty, !isWeakDefaultName(cleaned) { return cleaned }
            }
        }

        for prefix in ["iPhone von ", "iPad von "] where trimmed.hasPrefix(prefix) {
            let cleaned = trimmed.replacingOccurrences(of: prefix, with: "")
                .trimmingCharacters(in: .whitespacesAndNewlines)
            if !cleaned.isEmpty, !isWeakDefaultName(cleaned) { return cleaned }
        }

        return isWeakDefaultName(trimmed) ? nil : trimmed
    }

    /// Lustiger Random-Name als letzte Reißleine. Format: „Tier-XYZ" mit
    /// 3-stelliger Zahl. Eindeutig genug damit man sich gegenseitig
    /// unterscheiden kann ohne Code lesen zu müssen.
    private static func randomFunName() -> String {
        let animals = ["Adler", "Bär", "Delphin", "Eule", "Falke", "Gepard",
                       "Hirsch", "Igel", "Jaguar", "Koala", "Luchs", "Maus",
                       "Nashorn", "Otter", "Panda", "Quokka", "Robbe",
                       "Schwan", "Tiger", "Uhu", "Wolf", "Yak", "Zebra"]
        let n = Int.random(in: 100...999)
        return "\(animals.randomElement() ?? "Adler")-\(n)"
    }
}
